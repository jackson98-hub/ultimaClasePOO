    private void tbProductosMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbProductosMouseReleased
        int fila = tbProductos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe Seleccionar una Fila");
        } else {
            codproducto = Integer.parseInt(tbProductos.getValueAt(fila, 0).toString());
            txtCodigo.setText(tbProductos.getValueAt(fila, 0).toString());
            txtNombre.setText(tbProductos.getValueAt(fila, 1).toString());
            txtPrecio.setText(tbProductos.getValueAt(fila, 2).toString());
            txtStock.setText(tbProductos.getValueAt(fila, 3).toString());
            txtCategoria.setText(tbProductos.getValueAt(fila, 4).toString());

    }
    }//GEN-LAST:event_tbProductosMouseReleased

    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
         String mensaje = "";
        int fila = tbProductos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe selecionar una Fila");
        } else {
            mensaje = producto.actualizar(codproducto, txtNombre.getText(), Float.parseFloat(txtPrecio.getText()), Integer.parseInt(txtStock.getText()), Integer.parseInt(txtCategoria.getText()));
        }
        limpiarTabla();
        listar();
        lblMensaje.setText(mensaje);

    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        String mensaje = "";
        int fila = tbProductos.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Debe seleccionar una Fila");
        } else {
            mensaje = producto.eliminar(codproducto);
        }
        limpiarTabla();
        listar();
        btnNuevoActionPerformed(evt);
        lblMensaje.setText(mensaje);

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void txtBuscaKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscaKeyReleased
         if (!txtBusca.getText().isEmpty()) {
            codproducto = Integer.parseInt(txtBusca.getText());
            productoDTO = producto.buscar(codproducto);
            if (productoDTO != null) {
                limpiarTabla();
                modelo = (DefaultTableModel) tbProductos.getModel();
                Object[] ob = new Object[5];
                ob[0] = productoDTO.getCodproducto();
                ob[1] = productoDTO.getNombre();
                ob[2] = productoDTO.getPrecio();
                ob[3] = productoDTO.getStock();
                ob[4] = productoDTO.getCodcategoria();
                modelo.addRow(ob);
                tbProductos.setModel(modelo);
                lblMensaje.setText("Producto Encontrado");
            } else {
lblMensaje.setText("Producto No encontrado");
                limpiarTabla();
            }
        } else {
            limpiarTabla();
            listar();
            lblMensaje.setText("");
        }
    }//GEN-LAST:event_txtBuscaKeyReleased

