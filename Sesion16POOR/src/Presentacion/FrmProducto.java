    private void limpiarTabla() {
        modelo = (DefaultTableModel) tbProductos.getModel();
        modelo.getDataVector().removeAllElements();
        tbProductos.removeAll();
    }

    private void limpiarTexto() {
        txtCodigo.setText("");
        txtNombre.setText("");
        txtPrecio.setText("");
        txtStock.setText("");
        txtCategoria.setText("");
        txtCodigo.requestFocus();
    }

    private void listar() {
        try {
            modelo = (DefaultTableModel) tbProductos.getModel();
            Object[] ob = new Object[5];
            for (ProductoDTO p : producto.listar()) {
                ob[0] = p.getCodproducto();
                ob[1] = p.getNombre();
                ob[2] = p.getPrecio();
                ob[3] = p.getStock();
                ob[4] = p.getCodcategoria();
                modelo.addRow(ob);
            }
            tbProductos.setModel(modelo);
        } catch (Exception e) {
        }
        
    }

